class Solution {
  public:
    int maximumProfit(vector<int> &arr) {
        // code here
        int mine = arr[0];
        int profit = 0;
        for(int i =1;i<arr.size();i++){
            if(profit<(arr[i]-mine))
              profit = arr[i]-mine;
            mine = min(mine,arr[i]);
        }
        return profit;
    }
};


